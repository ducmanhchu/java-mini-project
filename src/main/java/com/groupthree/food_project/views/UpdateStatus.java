    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        String selectedStatus = (String) orderBox.getSelectedItem(); // Lấy giá trị của trạng thái được chọn
        // Cập nhật trạng thái trong đối tượng Order
        order.setStatus(selectedStatus);
        
        // Gọi phương thức cập nhật đơn hàng trong database
        boolean updated = orderDAO.updateOrder(order);
        if (updated) {
            JOptionPane.showMessageDialog(this, "Cập nhật trạng thái thành công!");
            this.dispose();
            OrderManagement orderManagement = new OrderManagement();
            orderManagement.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật trạng thái thất bại!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    
