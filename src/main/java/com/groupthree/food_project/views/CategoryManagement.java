    private void loadTableCategory(){
        // Lấy DefaultTableModel từ Jtable
        DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
       // Xóa dữ liệu trong JTable nếu có
        model.setRowCount(0);
        
        for(Category category : categories){
            model.addRow(new Object[] {
                category.getCategoryId(),
                category.getCategoryName()
            });
        }
        
        // Tạo renderer để căn giữa nội dung
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        // Lấy số lượng cột trong bảng
        int columnCount = tableCategory.getColumnModel().getColumnCount();

        // Duyệt qua tất cả các cột và đặt renderer
        for (int i = 0; i < columnCount; i++) {
            tableCategory.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }

    }
    
