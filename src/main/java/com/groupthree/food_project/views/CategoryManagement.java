    private void initTableCategoryListener(){
        tableCategory.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = tableCategory.getSelectedRow(); // Lấy dòng được chọn
                if (row >= 0) {
                    int categoryId = (int) tableCategory.getValueAt(row, 0); // Lấy ID danh mục
                    openProductDetails(categoryId);
                }
            }
        });
    }
    
    private void openProductDetails(int categoryId) {
        Category selectedCategory = categoryDAO.getCategoryById(categoryId);
        if (selectedCategory.getCategoryId()!= -1) {
            CategoryDetails categoryDetails = new CategoryDetails(selectedCategory);
            categoryDetails.setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
            categoryDetails.setVisible(true);
        }
    }

