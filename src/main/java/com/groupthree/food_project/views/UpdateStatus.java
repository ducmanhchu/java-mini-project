    private void loadOrderDetails() {
        OrderDetailDAO orderDetailDAO = new OrderDetailDAO();
        List<OrderDetail> details = orderDetailDAO.getOrderDetailsByOrderId(order.getOrderId());

        DefaultTableModel model = (DefaultTableModel) tableOrderDetails.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trước khi thêm mới

        for (OrderDetail detail : details) {
            model.addRow(new Object[]{
                detail.getOrderDetailId(),
                productDAO.getProductById(detail.getProductId()).getName(),
                detail.getQuantity(),
                detail.getUnitPrice()
            });
        }
        
        // Tạo renderer để căn giữa nội dung
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        // Lấy số lượng cột trong bảng
        int columnCount = tableOrderDetails.getColumnModel().getColumnCount();

        // Duyệt qua tất cả các cột và đặt renderer
        for (int i = 0; i < columnCount; i++) {
            tableOrderDetails.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
        
        // Hiện tổng tiền
        txtTotalPrice.setText(String.valueOf(order.getTotalPrice()));
    }
    
    public void fill(){
        txtOrderID.setText(String.valueOf(order.getOrderId()));
        loadOrderDetails();
        loadOrderBox();
    }

    // Lấy thông tin về các trạng thái đơn hàng và chọn mặc định
    private void loadOrderBox() {
        orderBox.removeAllItems(); // Xóa toàn bộ trạng thái trong JComboBox trước khi thêm mới

        String selectedStatus = null;
        if (order != null && order.getStatus() != null) {
            selectedStatus = order.getStatus();
        }

        //Thêm trạng thái mới vào orderBox và chọn mặc định
        List<String> statuses = orderDAO.getOrderStatuses(); // Lấy danh sách trạng thái từ DB
        for (String status : statuses) {
            orderBox.addItem(status);
            if (selectedStatus != null && status.equals(selectedStatus.trim())) {
                orderBox.setSelectedItem(status);
            }
        }

        // Nếu không có order hoặc status, chọn item đầu tiên nếu có
        if (order == null && orderBox.getItemCount() > 0) {
            orderBox.setSelectedIndex(0);
        }
    }
    
